    public FrameCarrinho() {
        initComponents();
        valorFrete = new BigDecimal("" + 0);
        valorTotal = new BigDecimal("" + 0);
        valorParcial = new BigDecimal("" + 0);
        setModal(true);

        BuscaDados buscar = new BuscaDados();

        ResultSet rsEnd;
        try {
            rsEnd = buscar.selectClienteEndereco(FramePrincipalCliente.getId_cliente());

            while (rsEnd.next()) {
                boxEndereco.addItem(rsEnd.getString("endereco"));
            }

            rsEnd.close();

        } catch (SQLException ex) {
            Logger.getLogger(FrameCarrinho.class.getName()).log(Level.SEVERE, null, ex);
        }

        Carrinho c = FramePrincipalCliente.getCarrinho();

        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < c.id_pratos.size(); i++) {
            BigDecimal precoItem = new BigDecimal("" + c.preco_pratos.get(i));
            BigDecimal quantidade = new BigDecimal("" + c.quantidades.get(i));
            precoItem = precoItem.multiply(quantidade);
            
            valorParcial = valorParcial.add(precoItem);
            model.addElement(c.nome_pratos.get(i) + " - " + c.quantidades.get(i) + " und. - " + "R$ " + precoItem);
        }
        listCarrinho.setModel(model);

        lblValorParcial.setText("R$ " + valorParcial);

        try {

            if (buscar.selectRestauranteEntrega(c.getRestaurante())) {
                //é entrega rápida
                valorFrete = valorFrete.add(new BigDecimal("" + 2));
                lblValorFrete.setText("R$ " + valorFrete);
            } else {
                lblValorFrete.setText("Grátis");

            }

        } catch (SQLException ex) {
            Logger.getLogger(FrameCarrinho.class.getName()).log(Level.SEVERE, null, ex);
        }

        valorTotal = valorTotal.add(valorParcial);
        valorTotal = valorTotal.add(valorFrete);
        lblValorTotal.setText("R$ " + valorTotal);

        this.model = model;

        repaint();
        revalidate();

    }

