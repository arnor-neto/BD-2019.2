    private void btnRemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveItemActionPerformed
        // TODO add your handling code here:

        Carrinho c = FramePrincipalCliente.getCarrinho();

        if (listCarrinho.getSelectedValue() == null) {
            //Não faz nada
        } else {
            int index = listCarrinho.getSelectedIndex();
            BigDecimal precoItem = new BigDecimal("" + c.preco_pratos.get(index));
            BigDecimal quantidade = new BigDecimal("" + c.quantidades.get(index));
            precoItem = precoItem.multiply(quantidade);
            
            // p isso funcionar, teria que colocar valorParcial e valorTotal como bigdecimal, e na hora d emnadar p o carrinho
            // mesmo acho que funcionaria por o (""+ tal) pq ele printa direitinho no sout

            valorParcial = valorParcial.subtract(precoItem);
            valorTotal = valorTotal.subtract(precoItem);
            
            model.remove(index);
            FramePrincipalCliente.getCarrinho().removeItem(index);

            lblValorParcial.setText("R$ " + valorParcial);
            lblValorTotal.setText("R$ " + valorTotal);

            listCarrinho.repaint();
            listCarrinho.revalidate();
        }
    }//GEN-LAST:event_btnRemoveItemActionPerformed

    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
        try {

            // TODO add your handling code here:
            if (listCarrinho.getLastVisibleIndex() == -1) {
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Você precisa ter pelo menos um item no \n carrinho para finalizar um pedido");
                this.setVisible(true);
            } else {

                Carrinho c = FramePrincipalCliente.getCarrinho();

                BuscaDados buscar = new BuscaDados();
                InsereDados inserir = new InsereDados();
                
                inserir.inserePedido(FramePrincipalCliente.getId_cliente(), c.getRestaurante(),
                        buscar.selectDate(), buscar.selectTime(), (String) boxEndereco.getSelectedItem(), "" + valorTotal, "" + valorFrete);
                
                int i = 0;
                for (int quantidade : c.quantidades) {
                    inserir.insereCarrinho(buscar.selectLastIdPedido(), c.id_pratos.get(i), quantidade);
                    i++;
                }
                
                c.esvaziaCarrinho();
                
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Pedido feito com sucesso. :D");
                this.dispose(); 
            }

        } catch (SQLException ex) {
            Logger.getLogger(FrameCarrinho.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnFinalizarActionPerformed

