    public void atualizaListas() throws SQLException {
        DefaultListModel modelAtuais = new DefaultListModel();
        DefaultListModel modelDisponiveis = new DefaultListModel();

        BuscaDados BD = new BuscaDados();
        ResultSet atuais = BD.selectRestauranteCategorias(FramePrincipalRestaurante.id_restaurante);
        while (atuais.next()) {
            modelAtuais.addElement(atuais.getString("nome_categoria") + " - Cód. categoria: " + atuais.getString("id_categoria"));
        }
        atuais.close();
        listCategoriasAtuais.setModel(modelAtuais);

        ResultSet disponiveis = BD.selectCategoriasExistentes();
        while (disponiveis.next()) {
            if (modelAtuais.contains(disponiveis.getString("nome_categoria") + " - Cód. categoria: " + disponiveis.getString("id_categoria"))) {

            } else {
                modelDisponiveis.addElement(disponiveis.getString("nome_categoria") + " - Cód. categoria: " + disponiveis.getString("id_categoria"));
            }
        }
        disponiveis.close();
        listCategoriasDisponiveis.setModel(modelDisponiveis);
    }

