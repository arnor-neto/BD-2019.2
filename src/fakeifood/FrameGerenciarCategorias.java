    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:

        String id_categoria = listCategoriasAtuais.getSelectedValue();
        if(listCategoriasAtuais.getLastVisibleIndex() == 0){
            JOptionPane.showMessageDialog(null, "Mantenha pelo menos uma categoria em seu restaurante");
        }else{
        if (id_categoria == null) {
            JOptionPane.showMessageDialog(null, "Selecione pelo menos uma categoria.");
        } else {
            int index = id_categoria.indexOf(":") + 2;
            id_categoria = id_categoria.substring(index);
            RemoveDados RD = new RemoveDados();
            RD.removePertencimento(FramePrincipalRestaurante.id_restaurante, Integer.parseInt(id_categoria));
            try {
                atualizaListas();
            } catch (SQLException ex) {
                Logger.getLogger(FrameGerenciarCategorias.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        InsereDados ID = new InsereDados();

        String id_categoria = listCategoriasDisponiveis.getSelectedValue();
        if (id_categoria == null) {
            JOptionPane.showMessageDialog(null, "Selecione pelo menos uma categoria.");
        } else {
            int index = id_categoria.indexOf(":") + 2;
            id_categoria = id_categoria.substring(index);
            ID.inserePertencimento(FramePrincipalRestaurante.id_restaurante, Integer.parseInt(id_categoria));
            try {
                atualizaListas();
            } catch (SQLException ex) {
                Logger.getLogger(FrameGerenciarCategorias.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed

