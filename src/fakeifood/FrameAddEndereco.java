    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        if (listEnderecos.getLastVisibleIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Você deve ter ao menos um endereço cadastrado.");
        } else {

            String endereco = listEnderecos.getSelectedValue();

            RemoveDados remover = new RemoveDados();

            remover.RemoveEndereco(endereco, FramePrincipalCliente.getId_cliente());

            try {
                atualizaLista();
            } catch (SQLException ex) {
                Logger.getLogger(FrameAddEndereco.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (fldRua.getText().isEmpty() || fldNumero.getText().isEmpty() || fldBairro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para adicionar um endereço.");
        } else {
            String Rua = fldRua.getText();
            String Numero = fldNumero.getText();
            String Bairro = fldBairro.getText();
            String Endereco = (Rua + ", " + Numero + ", " + Bairro);

            InsereDados inserir = new InsereDados();

            try {
                inserir.insereEndereco(Endereco, FramePrincipalCliente.getId_cliente());
                atualizaLista();
            } catch (SQLException ex) {
                Logger.getLogger(FrameAddEndereco.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

