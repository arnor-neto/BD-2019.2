    public void atualizaLista() throws SQLException {

        BuscaDados buscar = new BuscaDados();

        fldRua.setText("");
        fldNumero.setText("");
        fldBairro.setText("");

        ResultSet rs;

        rs = buscar.selectClienteEndereco(FramePrincipalCliente.getId_cliente());

        DefaultListModel model = new DefaultListModel();

        while (rs.next()) {
            model.addElement(rs.getString("endereco"));
        }

        rs.close();

        listEnderecos.setModel(model);

        listEnderecos.repaint();
        listEnderecos.revalidate();
    }

